package com.manchesterdigital.challenges;

public class Numbers {
    Integer a = 0;
    Integer b = 0;

    public Numbers(Integer a, Integer b) {
        this.a = a;
        this.b = b;
    }

    public Integer getA() {
        return a;
    }

    public void setA(Integer a) {
        this.a = a;
    }

    public Integer getB() {
        return b;
    }

    public void setB(Integer b) {
        this.b = b;
    }
}
